class Solution:
    def findFloor(self, arr, x):
        # code here
        left = 0
        right = len(arr) - 1
        ans = -1
        
        while left <= right :
            mid = (left + right) // 2
            
            if arr[mid] == x:
                ans = mid
                left = mid + 1
            
            elif arr[mid] < x:
                ans = mid
                left = mid + 1
                
            else:
                right = mid - 1
        
        return ans